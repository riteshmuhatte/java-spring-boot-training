public class Maths {
//sub division square cube	
	public void add(int a, int b) {
		int result = a + b;
		System.out.println("The Addition result is "+ result);
	}
	
	public void multiply(int a, int b) {
		int result = a * b;
		System.out.println("The Multiplication result is "+ result);
	}
	
	public void substract(int a, int b) {
		int result = a - b;
		System.out.println("The Substraction result is "+ result);
	}
	
	public void division(int a, int b) {
		int result = a * b;
		System.out.println("The Division result is "+ result);
	}
	
	public void square(int a) {
		int result = a * a;
		System.out.println("The Square result is "+ result);
	}
	
	public void cube(int a) {
		int result = a * a * a;
		System.out.println("The Cube result is "+ result);
	}
}